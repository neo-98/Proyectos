package calculadora.logic;

/**
 *
 * @author Estudiante
 */
public class Logica {

    public float Suma(float numberOne, float numberTwo) {
        float result = numberOne + numberTwo;
        return result;
    }

    public float Resta(float numberOne, float numberTwo) {
        float result = numberOne - numberTwo;
        return result;
    }

    public float Multiplicacion(float numberOne, float numberTwo) {
        float result = numberOne * numberTwo;
        return result;
    }

    public float Division(float numberOne, float numberTwo) {
        float result = numberOne / numberTwo;
        return result;
    }
}
